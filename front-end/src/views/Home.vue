<template>
<div class="home">
  <section class="image-gallery">
    <div class="image" v-for="recipe in recipes" :key="recipe.id">
      <h2>{{recipe.title}}</h2>
      <p>{{recipe.description}}</p>
      <img :src="recipe.path" />
    </div>
  </section>
</div>
</template>

