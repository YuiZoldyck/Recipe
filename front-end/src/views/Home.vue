<script>
// @ is an alias to /src
import axios from 'axios';
export default {
  name: 'HomeView',
  data() {
    return {
     recipes: [],
    }
  },
  created() {
    this.getRecipes();
  },
  methods: {
    async getRecipes() {
      try {
        let response = await axios.get("/api/recipes");
        this.recipes = response.data;
        return true;
      } catch (error) {
        // eslint-disable-next-line
        console.log(error);
      }
    },
  }
}
</script>
